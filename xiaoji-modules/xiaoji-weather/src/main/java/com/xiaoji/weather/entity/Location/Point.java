package com.xiaoji.weather.entity.Location;

public class Point {

    private String y;
    private String x;
    public void setY(String y) {
        this.y = y;
    }
    public String getY() {
        return y;
    }

    public void setX(String x) {
        this.x = x;
    }
    public String getX() {
        return x;
    }

}